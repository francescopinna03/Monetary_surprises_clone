function result = Time_alignment_self_test()
%TIME_ALIGNMENT_SELF_TEST Deterministic DST tests for ECB/Barchart alignment.
%
% These cases cover ordinary summer time and the March weeks in which the US
% and Europe change clocks on different dates. A fixed six- or seven-hour
% subtraction fails at least one case; IANA timezone conversion must align all
% pairs exactly.

    cfg = Time_alignment_config();

    ecbWall = datetime([ ...
        2020 3 12 13 45 0; ...
        2020 12 10 13 45 0; ...
        2023 3 16 14 15 0; ...
        2023 9 14 14 15 0; ...
        2024 6  6 14 15 0]);

    barchartWall = datetime([ ...
        2020 3 12 7 45 0; ...
        2020 12 10 6 45 0; ...
        2023 3 16 8 15 0; ...
        2023 9 14 7 15 0; ...
        2024 6  6 7 15 0]);

    expectedUtc = datetime([ ...
        2020 3 12 12 45 0; ...
        2020 12 10 12 45 0; ...
        2023 3 16 13 15 0; ...
        2023 9 14 12 15 0; ...
        2024 6  6 12 15 0]);

    ecbUtc = Wall_clock_to_utc(ecbWall, cfg.event_time_zone);
    barchartUtc = Wall_clock_to_utc(barchartWall, cfg.raw_time_zone);

    if ~all(ecbUtc == expectedUtc) || ~all(barchartUtc == expectedUtc) || ~all(ecbUtc == barchartUtc)
        error('TIME_ALIGNMENT_SELF_TEST_FAILED: IANA timezone conversion did not reproduce the frozen reference cases.');
    end

    result = table(ecbWall, barchartWall, ecbUtc, expectedUtc, ...
        'VariableNames', {'ecb_wall_europe_berlin', 'barchart_wall_america_chicago', 'computed_utc', 'expected_utc'});

    fprintf('Time-alignment self-test: PASS (%d DST reference cases).\n', height(result));
end
